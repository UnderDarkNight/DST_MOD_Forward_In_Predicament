------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
--- 修改官方动作 RUMMAGE 添加距离。让 渔场可以远一点打开
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
local RUMMAGE = ACTIONS.RUMMAGE
if RUMMAGE then
    if RUMMAGE.distance == nil or RUMMAGE.distance < 1.2 then
        RUMMAGE.distance = 1.2
    end

end