--- 让 蜂后掉落 昆虫法杖
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
AddPrefabPostInit(
    "beequeen",
    function(inst)
        if not TheWorld.ismastersim then
            return
        end

        inst:ListenForEvent("death",function()
            if inst.components.lootdropper then
                inst.components.lootdropper:SpawnLootPrefab("fwd_in_pdt_equipment_insect_staff")
            end
        end)

    end
)



